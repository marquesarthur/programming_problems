from bisect import bisect_left, bisect_right

class OptimalSolution:
    # @param A : tuple of integers
    # @param B : integer
    # @return a list of integers
    def searchRange(self, A, B):
        l, r = bisect_left(A, B), bisect_right(A, B)
        if l == r:
            return [-1, -1]
        else:
            return [l, r-1]


class Solution:
    def binSearchLeft(self, A, B, begin, end):
        # find biggest index smaller or equal to B
        if begin > end:
            return -1
        else:
            mid = int((begin + end) / 2)
            if A[mid] == B:
                result = mid
                left = self.binSearchLeft(A, B, begin, mid - 1)
                if left != -1:
                    result = min(result, left)



                return result

            elif A[mid] < B:
                return self.binSearchLeft(A, B, mid + 1, end)

            else:
                return self.binSearchLeft(A, B, begin, mid - 1)

    def binSearchRight(self, A, B, begin, end):

        # find smalles index greater or equal to B


        if begin > end:
            return -1
        else:
            mid = int((begin + end) / 2)

            if A[mid] == B:
                result = mid

                right = self.binSearchRight(A, B, mid + 1, end)
                if right != -1:
                    result = max(result, right)

                return result
            elif A[mid] > B:
                return self.binSearchRight(A, B, begin, mid - 1)
            else:
                return self.binSearchRight(A, B, mid + 1, end)

    # @param A : tuple of integers
    # @param B : integer
    # @return a list of integers
    def searchRange(self, A, B):

        # example:
        # 5   7   7   7   8   8   10
        # 0   1   2   3   4   5   6

        # search for 1st i >= B
        # search for first j <= B and j > i
        # problem resumes to finding i

        i = self.binSearchLeft(A, B, 0, len(A) - 1)
        j = self.binSearchRight(A, B, 0, len(A) - 1)

        if i != -1 and j != -1:
            return [i, j]

        return [-1, -1]


